function snakeLaddersBoard(height) {
    let result = []
    var count = Math.pow(height,2)

    for(let i = 0; i < height; i++) {
      let arr = []
      let tempCount = count
      for (let j = height; j > 0; j--) {
        if(height % 2 === 0) {
            if(i%2 === 1) {
                arr.push(tempCount-(j-1));
            } else if (i%2 === 0) {
                arr.push(count);
            }
            count -= 1
        }else if(height % 2 === 1) {
            if(i%2 === 0) {
                arr.push(tempCount-(j-1));
            } else if(i%2 === 1) {
                arr.push(count);
            }
            count -= 1
        }
      }
      result.push(arr);
    }
    return result
}

